import assert from 'node:assert/strict';
import { mkdirSync, writeFileSync } from 'node:fs';
import { canonicalInput } from './passive-live-memory-coherence-repeatability-scorecard-fixtures.mjs';
import { scorePassiveLiveMemoryCoherenceRepeatabilityScorecard } from '../src/passive-live-memory-coherence-repeatability-scorecard.mjs';
const out = scorePassiveLiveMemoryCoherenceRepeatabilityScorecard(canonicalInput());
assert.equal(out.repeatabilityStatus, 'PASSIVE_LIVE_MEMORY_COHERENCE_REPEATABILITY_SCORECARD_COMPLETE');
assert.equal(out.validationIssues.length, 0);
assert.equal(out.runSummaries.length, 3);
assert.deepEqual(out.runSummaries.map((r)=>r.variant), ['baseline_order','paraphrased_observation_text','reverse_order']);
assert.ok(out.runSummaries.every((r)=>r.observationJudgementCount === 4 && r.policyDecision === null && r.agentConsumedOutput === false));
assert.equal(out.repeatabilityItems.length, 4);
assert.ok(out.repeatabilityItems.every((item)=>item.runCount === 3 && item.stableReceiverLabel === true && item.agreesWithV036Observation === true));
mkdirSync('evidence',{recursive:true});
writeFileSync('evidence/passive-live-memory-coherence-repeatability-scorecard-runs-v0.37.1.out.json', JSON.stringify({ repeatabilityStatus: out.repeatabilityStatus, runSummaries: out.runSummaries, repeatabilityItems: out.repeatabilityItems }, null, 2)+'\n');
