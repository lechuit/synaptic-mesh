import assert from 'node:assert/strict';
import { mkdirSync, writeFileSync } from 'node:fs';
import { canonicalInput } from './passive-hard-case-outcome-value-scorecard-fixtures.mjs';
import { scorePassiveHardCaseOutcomeValueScorecard } from '../src/passive-hard-case-outcome-value-scorecard.mjs';
const out = scorePassiveHardCaseOutcomeValueScorecard(canonicalInput());
assert.equal(out.outcomeValueStatus, 'PASSIVE_HARD_CASE_OUTCOME_VALUE_SCORECARD_COMPLETE');
assert.equal(out.outcomeItems.length, 5);
assert.equal(out.outcomeItems.filter(o=>o.outcomeValue === 'useful').length, 3);
assert.equal(out.outcomeItems.filter(o=>o.outcomeValue === 'noise').length, 1);
assert.equal(out.outcomeItems.filter(o=>o.outcomeValue === 'evidence_gap').length, 1);
assert.ok(out.outcomeItems.every(o=>o.sourceBound === true && o.humanReviewOnly === true && o.promoteToMemory === false && o.policyDecision === null));
mkdirSync('evidence',{recursive:true}); writeFileSync('evidence/passive-hard-case-outcome-value-scorecard-outcomes-v0.34.1.out.json', JSON.stringify(out.outcomeItems,null,2)+'\n');
