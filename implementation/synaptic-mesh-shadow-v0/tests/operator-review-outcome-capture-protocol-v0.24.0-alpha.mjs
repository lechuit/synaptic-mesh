import assert from 'node:assert/strict';
import { mkdir, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';
import { operatorReviewOutcomeCaptureProtocol, OPERATOR_REVIEW_OUTCOME_LABELS } from '../src/operator-review-outcome-capture.mjs';

await mkdir(resolve('evidence'), { recursive: true });
const protocol = operatorReviewOutcomeCaptureProtocol();
assert.equal(protocol.releaseLayer, 'v0.24.0-alpha');
assert.equal(protocol.manualOperatorRunOnly, true);
assert.equal(protocol.localOnly, true);
assert.equal(protocol.oneShot, true);
assert.equal(protocol.redactionBeforePersist, true);
assert.equal(protocol.humanReadableOnly, true);
assert.equal(protocol.nonAuthoritative, true);
assert.equal(protocol.valueFeedbackOnly, true);
assert.equal(protocol.notPolicyArtifact, true);
assert.equal(protocol.policyDecision, null);
for (const key of ['authorization','enforcement','approvalBlockAllow','toolExecution','memoryConfigWrite','networkResourceFetch','externalEffects','rawPersisted','rawOutput','agentConsumedOutput','machineReadablePolicyDecision','runtimeAuthority']) assert.equal(protocol[key], false, key);
assert.deepEqual(protocol.allowedOutcomeLabels, OPERATOR_REVIEW_OUTCOME_LABELS);
await writeFile(resolve('evidence/operator-review-outcome-capture-protocol-v0.24.0-alpha.out.json'), JSON.stringify(protocol, null, 2) + '\n');
console.log(JSON.stringify({ protocol: 'ok', labels: protocol.allowedOutcomeLabels.length }, null, 2));
