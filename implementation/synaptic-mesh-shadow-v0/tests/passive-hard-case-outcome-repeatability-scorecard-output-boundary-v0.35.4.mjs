import assert from 'node:assert/strict';
import { mkdirSync, writeFileSync } from 'node:fs';
import { canonicalInput, clone } from './passive-hard-case-outcome-repeatability-scorecard-fixtures.mjs';
import { scorePassiveHardCaseOutcomeRepeatabilityScorecard, validatePassiveHardCaseOutcomeRepeatabilityScorecardArtifact } from '../src/passive-hard-case-outcome-repeatability-scorecard.mjs';
const out = scorePassiveHardCaseOutcomeRepeatabilityScorecard(canonicalInput());
assert.equal(out.repeatabilityStatus, 'PASSIVE_HARD_CASE_OUTCOME_REPEATABILITY_SCORECARD_COMPLETE');
assert.equal(out.policyDecision, null);
assert.equal(out.recommendationIsAuthority, false);
assert.equal(out.agentConsumedOutput, false);
assert.equal(out.notRuntimeInstruction, true);
assert.equal(out.noRuntimeAuthority, true);
assert.equal(out.noMemoryWrites, true);
assert.equal(out.noRuntimeIntegration, true);
assert.equal(out.rawPersisted, false);
assert.ok(out.repeatabilityItems.every((item)=>item.policyDecision === null && item.promoteToMemory === false && item.agentConsumedOutput === false));
assert.deepEqual(validatePassiveHardCaseOutcomeRepeatabilityScorecardArtifact(out), []);
const poisoned = clone(out);
poisoned.repeatabilityItems[0].agentConsumedOutput = true;
assert.ok(validatePassiveHardCaseOutcomeRepeatabilityScorecardArtifact(poisoned).some((issue)=>issue.includes('boundary')));
const tokened = clone(out);
tokened.repeatabilityItems[0].labelsByRun[0].receiverLabel = 'approve-secret';
const tokenIssues = validatePassiveHardCaseOutcomeRepeatabilityScorecardArtifact(tokened);
assert.ok(tokenIssues.some((issue)=>issue.includes('authority_token_detected')));
assert.ok(!JSON.stringify(scorePassiveHardCaseOutcomeRepeatabilityScorecard((()=>{const input=clone(canonicalInput()); input.repeatabilityRuns[0].receiverOutcomes[0].receiverRationale='approve-secret'; return input;})())).includes('approve-secret'));
mkdirSync('evidence',{recursive:true});
writeFileSync('evidence/passive-hard-case-outcome-repeatability-scorecard-output-boundary-v0.35.4.out.json', JSON.stringify({ status: 'pass', validationIssues: validatePassiveHardCaseOutcomeRepeatabilityScorecardArtifact(out), policyDecision: out.policyDecision, recommendationIsAuthority: out.recommendationIsAuthority, agentConsumedOutput: out.agentConsumedOutput }, null, 2)+'\n');
