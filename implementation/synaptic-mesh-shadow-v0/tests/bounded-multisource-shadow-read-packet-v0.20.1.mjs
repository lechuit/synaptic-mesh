import assert from 'node:assert/strict';
import { mkdir, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';
import { BOUNDED_MULTISOURCE_SHADOW_READ_ADAPTER_ID, runBoundedMultisourceShadowRead } from '../src/bounded-multisource-shadow-read.mjs';

await mkdir(resolve('evidence'), { recursive: true });
const packet = JSON.parse(await runBoundedMultisourceShadowRead({ sources: ['README.md', 'RELEASE_NOTES.md', 'RELEASE_CANDIDATE.md'], adapter: BOUNDED_MULTISOURCE_SHADOW_READ_ADAPTER_ID, recordsPerSource: 4, totalRecords: 10 }));
assert.equal(packet.summary.boundedExplicitMultisourceShadowReadBarrierCrossed, true);
assert.equal(packet.summary.boundedExplicitMultiSourceShadowRead, true);
assert.equal(packet.summary.constrainedLocalReadAdapter, true);
assert.equal(packet.summary.sourceCount, 3);
assert.equal(packet.summary.recordsRead <= 10, true);
assert.equal(packet.summary.recordsPerSourceLimit, 4);
assert.equal(packet.summary.policyDecision, null);
for (const key of ['rawPersisted','rawOutput','agentConsumedOutput','externalEffects','enforcement','authorization','approvalBlockAllow','toolExecution','memoryConfigWrite','watcherDaemon','autonomousLiveMode','networkResourceFetch','globRecursiveDiscovery','implicitSources','outsideRepoPaths','symlinks']) assert.equal(packet.summary[key], false, key);
assert.equal(packet.summary.unexpectedPermits, 0);
assert.equal(packet.sources.every((source) => source.rawSourcePathPersisted === false), true);
assert.equal(packet.sources.every((source) => source.status === 'ok'), true);
assert.equal(packet.usefulnessScorecard.crossedBeyondSingleSourceAdapterShadowRead, true);
assert.equal(packet.usefulnessScorecard.usedMultipleRealRepoLocalSources, true);
assert.match(packet.reportMarkdown, /Bounded Explicit Multisource Shadow-Read Report v0\.20\.5/);
await writeFile(resolve('evidence/bounded-multisource-shadow-read-packet-v0.20.1.out.json'), JSON.stringify(packet, null, 2) + '\n');
await writeFile(resolve('evidence/bounded-multisource-shadow-read-report-v0.20.1.out.md'), packet.reportMarkdown);
console.log(JSON.stringify(packet.summary, null, 2));
