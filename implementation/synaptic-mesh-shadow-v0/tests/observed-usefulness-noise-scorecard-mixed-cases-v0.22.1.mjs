import assert from 'node:assert/strict';
import { mkdir, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';
import { scoreObservedUsefulnessNoise } from '../src/observed-usefulness-noise-scorecard.mjs';
import { observedUsefulnessNoiseCases } from './observed-usefulness-noise-scorecard-fixtures.mjs';

await mkdir(resolve('evidence'), { recursive: true });
const cases = await observedUsefulnessNoiseCases();
const scorecard = scoreObservedUsefulnessNoise(cases);
assert.equal(scorecard.metrics.totalCases, 9);
assert.equal(scorecard.metrics.trueUsefulPasses, 3);
assert.equal(scorecard.metrics.falsePasses, 0);
assert.equal(scorecard.metrics.noisyRejects, 6);
assert.equal(scorecard.metrics.noiseRejected, 6);
assert.equal(scorecard.metrics.missedUsefulPasses, 0);
assert.equal(scorecard.metrics.passPrecision, 1);
assert.equal(scorecard.metrics.passUsefulness, 1);
assert.equal(scorecard.recommendation, 'advance');
await writeFile(resolve('evidence/observed-usefulness-noise-scorecard-mixed-cases-v0.22.1.out.json'), JSON.stringify({ cases: scorecard.cases, metrics: scorecard.metrics, recommendation: scorecard.recommendation }, null, 2) + '\n');
console.log(scorecard.reportMarkdown);
