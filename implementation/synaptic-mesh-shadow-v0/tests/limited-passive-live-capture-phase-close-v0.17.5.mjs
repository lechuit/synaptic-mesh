const out = { artifact:'T-synaptic-mesh-limited-passive-live-capture-phase-close-v0.17.5', timestamp:'2026-05-15T10:00:00.000Z', summary:{ ...summary, phaseClosed:true, unexpectedPermits:0, noAutonomousLiveMode:true }};
await writeFile(resolve(pkg,'evidence/limited-passive-live-capture-phase-close-v0.17.5.out.json'), JSON.stringify(out,null,2)+'\n');
