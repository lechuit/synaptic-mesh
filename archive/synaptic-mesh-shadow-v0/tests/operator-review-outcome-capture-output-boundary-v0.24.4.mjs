import assert from 'node:assert/strict';
import { execFileSync } from 'node:child_process';
import { mkdir, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';
import { operatorReviewQueueFixture, operatorOutcomesFixture } from './operator-review-outcome-capture-fixtures.mjs';

await mkdir(resolve('evidence'), { recursive: true });
const queue = await operatorReviewQueueFixture();
const outcomes = await operatorOutcomesFixture();
await writeFile(resolve('evidence/operator-review-outcome-capture-queue-input-v0.24.4.json'), JSON.stringify(queue, null, 2) + '\n');
await writeFile(resolve('evidence/operator-review-outcome-capture-outcomes-input-v0.24.4.json'), JSON.stringify(outcomes, null, 2) + '\n');
const report = execFileSync(process.execPath, ['bin/operator-review-outcome-capture.mjs', 'evidence/operator-review-outcome-capture-queue-input-v0.24.4.json', 'evidence/operator-review-outcome-capture-outcomes-input-v0.24.4.json'], { encoding: 'utf8' });
assert.match(report, /Operator Review Outcome Capture v0\.24\.5/);
assert.match(report, /human-readable report only/);
assert.match(report, /policyDecision: null/);
assert.match(report, /rawPersisted: false; rawOutput: false/);
assert.doesNotMatch(report, /person@example\.com/);
assert.doesNotMatch(report, /\{\s*"artifact"/);
await writeFile(resolve('evidence/operator-review-outcome-capture-report-v0.24.4.out.md'), report);
console.log(JSON.stringify({ report: 'human-readable-only', redacted: !report.includes('person@example.com') }, null, 2));
