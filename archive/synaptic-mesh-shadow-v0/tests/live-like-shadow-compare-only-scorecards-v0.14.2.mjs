import assert from 'node:assert/strict';
import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { replayFrozenLiveLikeEnvelopes } from '../src/live-like-shadow-replay.mjs';
import { buildCompareOnlyScorecard } from '../src/live-like-shadow-scorecard.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const packageRoot = resolve(here, '..');
const repoRoot = resolve(packageRoot, '../..');
const evidencePath = resolve(packageRoot, 'evidence/live-like-shadow-compare-only-scorecards-v0.14.2.out.json');
const docs = await readFile(resolve(repoRoot, 'docs/live-like-shadow-compare-only-scorecards-v0.14.2.md'), 'utf8');
const missing = ['compare-only', 'advisory/comparison evidence only', 'not policy decisions', 'not agent-consumed output', 'not runtime authority', 'release:check -- --target v0.14.2'].filter((phrase) => !docs.includes(phrase));
assert.deepEqual(missing, [], 'docs must include required phrases');
const fixture = JSON.parse(await readFile(resolve(packageRoot, 'fixtures/live-like-shadow-observation-v0.14.0-alpha.json'), 'utf8'));
const replay = replayFrozenLiveLikeEnvelopes([fixture]);
const summary = buildCompareOnlyScorecard(replay);
assert.equal(summary.mode, 'compare-only');
assert.equal(summary.evidenceOnly, true);
assert.equal(summary.policyDecision, false);
assert.equal(summary.agentConsumed, false);
assert.equal(summary.notRuntimeAuthority, true);
assert.equal(summary.comparison.unexpectedPermits, 0);
const output = { artifact: 'T-synaptic-mesh-live-like-shadow-compare-only-scorecards-v0.14.2', timestamp: '2026-05-14T23:59:00.000Z', summary };
await mkdir(dirname(evidencePath), { recursive: true });
await writeFile(evidencePath, JSON.stringify(output, null, 2) + '\n');
console.log(JSON.stringify(summary, null, 2));
