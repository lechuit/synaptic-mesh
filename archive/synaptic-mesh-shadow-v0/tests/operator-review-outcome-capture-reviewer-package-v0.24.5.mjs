import assert from 'node:assert/strict';
import { mkdir, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';
import { buildOperatorReviewOutcomeCapture } from '../src/operator-review-outcome-capture.mjs';
import { operatorReviewQueueFixture, operatorOutcomesFixture } from './operator-review-outcome-capture-fixtures.mjs';

await mkdir(resolve('evidence'), { recursive: true });
const queue = await operatorReviewQueueFixture();
const outcomes = await operatorOutcomesFixture();
const capture = buildOperatorReviewOutcomeCapture(queue, outcomes);
assert.equal(capture.releaseLayer, 'v0.24.5');
assert.equal(capture.captureStatus, 'OUTCOME_CAPTURE_COMPLETE');
assert.equal(capture.capturedOutcomeCount, 3);
assert.equal(capture.humanReadableOnly, true);
assert.equal(capture.nonAuthoritative, true);
assert.equal(capture.valueFeedbackOnly, true);
assert.equal(capture.policyDecision, null);
for (const key of ['authorization','enforcement','approvalBlockAllow','toolExecution','memoryConfigWrite','networkResourceFetch','externalEffects','rawPersisted','rawOutput','agentConsumedOutput','machineReadablePolicyDecision','runtimeAuthority']) assert.equal(capture[key], false, key);
assert.equal(capture.falseAuthorityLeakage.length, 0);
await writeFile(resolve('evidence/operator-review-outcome-capture-reviewer-package-v0.24.5.out.json'), JSON.stringify(capture, null, 2) + '\n');
await writeFile(resolve('evidence/operator-review-outcome-capture-report-v0.24.5.out.md'), capture.reportMarkdown);
console.log(JSON.stringify({ captureStatus: capture.captureStatus, capturedOutcomeCount: capture.capturedOutcomeCount, falseAuthorityLeakage: capture.falseAuthorityLeakage.length }, null, 2));
